'''
Coming up with a even Fibonnaci sequence rule
Proven by induction
3 = 1 + 2
5 = 2 + 3
8 = 3 + 5
13= 5 + 8
21= 8 + 13
34= 13 + 21
55= 21 + 34
89= 34 + 55
144=55 + 89

8 = (1 + 2) + 2 + (1 + 2) = 2*1 + 3*2
34 = 2 + 1 + 2 + 8 + 8 + 2 + 1 + 2 + 8 = 2*1 + 4*2 + 3*8 = 8 + 2 + 3*8 = 2 + 4*8
144= 8 + 2 + 1 + 2 + 8 + 34 + 34 + 8 + 2 + 1 + 2 + 8 + 34 = 2*1 + 4*2 + 4*8 + 3*34 = 8 + 4*34
...
2*1 + 4*2 + 4*8 + 4*34 * 3*144 = 610
2*1 + 4*2 + 4*8 + 4*34 * 3*144 + 144 + 3*610 = 2584
'''

a = 2
b = 8
even = [a]

while b < 4000000:
    aux = a + 4*b
    a = b
    b = aux

    even.append(a)

total_sum = sum(even)
